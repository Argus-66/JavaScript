// For loop
let arr = [1, 2, 3, 4, 5];
for (let i = 0; i < arr.length; i++) {
    console.log(arr[i]);
}

console.log("------------------------------")

// While loop

let j = 0;
while (j < arr.length) {
    console.log(arr[j]);
    j++;
}

console.log("------------------------------")

